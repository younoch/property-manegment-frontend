}

interface Property {
  name: string
  unit: string
  period: string
  dueDate: string
}

interface Item {
  description: string
  qty: number
  rate: number
}

interface Payment {
  bankName: string
  bank: string
  account: string
  swift: string
}

const props = defineProps<{
  invoiceNumber: string
  invoiceDate: string
  tenant: Tenant
  property: Property
  items: Item[]
  taxRate: number
  payment: Payment
}>()

// computed totals
const subtotal = computed(() =>
  props.items.reduce((sum, item) => sum + item.qty * item.rate, 0)
)
const taxAmount = computed(() => (subtotal.value * props.taxRate) / 100)
const totalDue = computed(() => subtotal.value + taxAmount.value)

